    private void btn_entradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_entradaActionPerformed
        // TODO add your handling code here:
       String articulo = txtarticulo.getText().trim();
    String tipo = txttipo.getText().trim();
    String entradaTexto = txtentrada.getText().trim();

    if (articulo.isEmpty() || tipo.isEmpty() || entradaTexto.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Completa todos los campos antes de registrar la entrada.");
        return;
    }

    int entrada;
    try {
        entrada = Integer.parseInt(entradaTexto);
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "La entrada debe ser un número.");
        return;
    }

    boolean existe = false;

    // Buscar si el artículo ya existe en la tabla principal
    for (int i = 0; i < modeloTabla.getRowCount(); i++) {
        if (modeloTabla.getValueAt(i, 0).toString().equalsIgnoreCase(articulo)) {
            int stockactual = Integer.parseInt(modeloTabla.getValueAt(i, 4).toString());
            int nuevostock = stockactual + entrada;
            modeloTabla.setValueAt(entrada, i, 2);   // entrada
            modeloTabla.setValueAt("", i, 3);        // limpiar salida
            modeloTabla.setValueAt(nuevostock, i, 4);
            existe = true;
            break;
        }
    }

    // Si no existe, lo agregamos como nuevo
    if (!existe) {
        modeloTabla.addRow(new Object[]{articulo, tipo, entrada, "", entrada});
    }

    // Agregar movimiento al historial (tablaarticulo2)
    modeloTabla2.addRow(new Object[]{articulo, tipo, entrada, "", getStockActual(articulo)});

    // Limpiar campos
    txtarticulo.setText("");
    txttipo.setText("");
    txtentrada.setText("");
    }//GEN-LAST:event_btn_entradaActionPerformed

    private void btn_salidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salidaActionPerformed
        // TODO add your handling code here:
        String articulo = txtarticulo.getText().trim();
    String tipo = txttipo.getText().trim();
    String salidaTexto = txtsalida.getText().trim();

    if (articulo.isEmpty() || tipo.isEmpty() || salidaTexto.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Completa todos los campos antes de registrar la salida.");
        return;
    }

    int salida;
    try {
        salida = Integer.parseInt(salidaTexto);
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "La salida debe ser un número.");
        return;
    }

    boolean encontrado = false;

   
    for (int i = 0; i < modeloTabla.getRowCount(); i++) {
        if (modeloTabla.getValueAt(i, 0).toString().equalsIgnoreCase(articulo)) {
            int stockactual = Integer.parseInt(modeloTabla.getValueAt(i, 4).toString());
            int nuevostock = stockactual - salida;
            if (nuevostock < 0) nuevostock = 0;

            modeloTabla.setValueAt("", i, 2);     
            modeloTabla.setValueAt(salida, i, 3);  
            modeloTabla.setValueAt(nuevostock, i, 4);
            encontrado = true;
            break;
        }
    }

    if (!encontrado) {
        javax.swing.JOptionPane.showMessageDialog(this, "El artículo no existe en la tabla principal.");
    } else {
        
        modeloTabla2.addRow(new Object[]{articulo, tipo, "", salida, getStockActual(articulo)});
    }
    txtarticulo.setText("");
    txttipo.setText("");
    txtsalida.setText("");
    }//GEN-LAST:event_btn_salidaActionPerformed
private int getStockActual(String articulo) {
    for (int i = 0; i < modeloTabla.getRowCount(); i++) {
        if (modeloTabla.getValueAt(i, 0).toString().equalsIgnoreCase(articulo)) {
            return Integer.parseInt(modeloTabla.getValueAt(i, 4).toString());
        }
    }
    return 0;
}
