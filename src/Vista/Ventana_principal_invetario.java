    private void mostrarContenido(String nombreNodo) {
    // Reinicia todo
    panelColor.setBackground(null);
    lblImagen.setIcon(null);
    lblImagen.setText("");

    switch (nombreNodo.toLowerCase()) {
        // Colores → se pintan en el panel
        case "rojo":
            panelColor.setBackground(Color.RED);
            break;
        case "verde":
            panelColor.setBackground(Color.GREEN);
            break;
        case "azul":
            panelColor.setBackground(Color.BLUE);
            break;

        //  Autos → se muestran en el label
        case "skin":
            mostrarImagen("/img/skin.png");
            break;
        case "nails":
            mostrarImagen("/img/nails.png");
            break;
        case "avion":
            mostrarImagen("/img/avion.png");
            break;

        //  Animales
        case "perro":
            mostrarImagen("/img/perro.png");
            break;
        case "gato":
            mostrarImagen("/img/gato.png");
            break;
        case "raton":
            mostrarImagen("/img/raton.png");
            break;

        default:
            // Si el nodo no tiene color ni imagen
            lblImagen.setText("Sin contenido visual");
    }
}

    private void mostrarImagen(String ruta) {
    java.net.URL url = getClass().getResource(ruta);
    if (url != null) {
        javax.swing.ImageIcon icon = new javax.swing.ImageIcon(url);
        lblImagen.setIcon(icon);
    } else {
        lblImagen.setText("Imagen no encontrada");
    }
    }
    
    
    
