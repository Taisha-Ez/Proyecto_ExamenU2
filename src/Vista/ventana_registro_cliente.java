    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        String nombre = txtnombre.getText();
        String pass = new String(txtcontra.getPassword());
        String confirmar = new String(txtconfirmar.getPassword());

    if (nombre.isEmpty() || pass.isEmpty() || confirmar.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor llena todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    if (!pass.equals(confirmar)) {
        JOptionPane.showMessageDialog(this, "Las contraseÃ±as no coinciden", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    // ðŸ”¹ Guardar usuario en ControlUsuarios
    cControlUsuarios.registrarUsuario(nombre, pass);
    JOptionPane.showMessageDialog(this, "âœ… Registro exitoso para: " + nombre);

    // Volver al login
    new ventana_cliente().setVisible(true);
    this.dispose();
    }//GEN-LAST:event_btnregistrarActionPerformed

